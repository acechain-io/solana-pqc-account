//! Verify and print the circuit IDs used by ACE Account Kit.
//!
//! Run: cargo run --release --bin extract_circuit_id

use sha2::{Digest, Sha256};

fn compute(label: &str) -> [u8; 32] {
    let hash = Sha256::digest(label.as_bytes());
    let mut out = [0u8; 32];
    out.copy_from_slice(&hash);
    out
}

fn main() {
    let entries = [
        ("ZK-ACE-STWO-v1:NonceRegistry",    "get_zk_ace_circuit_id"),
        ("ZK-OWNERSHIP-STWO-v1:Native",     "get_zk_ownership_circuit_id"),
    ];

    println!("// Circuit IDs for programs/ace-account-kit/src/vk.rs");
    println!("// Generated by: cargo run --release --bin extract_circuit_id");
    println!();

    for (label, fn_name) in &entries {
        let id = compute(label);
        println!("// SHA-256(\"{label}\")");
        println!("pub fn {fn_name}() -> [u8; 32] {{");
        print!("    [");
        for (i, b) in id.iter().enumerate() {
            if i % 8 == 0 { print!("\n        "); }
            print!("0x{b:02x}");
            if i < 31 { print!(", "); }
        }
        println!("\n    ]");
        println!("}}");
        println!();
    }
}
